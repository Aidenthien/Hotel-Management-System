    public void populateTable() {
    String[] columns = {"Name", "Identity Card/Passport", "Gender", "Phone Number", "Email", "Country", "Check In", "Check Out", "Room ID", "Price"};
    DefaultTableModel model = new DefaultTableModel(columns, 0){
    public boolean isCellEditable(int row, int column){
        return false;
    }
};
    
    //bufferReader reads data faster and efficient only
    try (BufferedReader br = new BufferedReader(new FileReader("bookingDetails.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(", ");
            model.addRow(data);
        }
    } catch (IOException e) {
        e.printStackTrace();
    }

    jTable1.setModel(model);
}


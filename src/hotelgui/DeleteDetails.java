    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = jTable1.getSelectedRow();
            if (selectedRow != -1) {
                String customerName = (String) jTable1.getValueAt(selectedRow,0);
                String customerIdentity = (String) jTable1.getValueAt(selectedRow, 1);
                String customerPhone = (String) jTable1.getValueAt(selectedRow, 3);
                String customerEmail = (String) jTable1.getValueAt(selectedRow, 4);
                String customerRoom = (String) jTable1.getValueAt(selectedRow, 8);
                int n = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this booking?", "Confirmation", JOptionPane.YES_NO_OPTION);
                if (n==JOptionPane.YES_OPTION){
                //audit log for delete is here
                auditLog log = new auditLog();
                log.auditLog("Customer " + customerName + "'s booking for room "+customerRoom+" has been cancelled and removed");
                Delete delete = new Delete();
                boolean deleted = delete.deleteDetails("bookingDetails.txt", customerIdentity, customerPhone, customerEmail, customerRoom);
                if (deleted) {
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(selectedRow);
                }
                }
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

